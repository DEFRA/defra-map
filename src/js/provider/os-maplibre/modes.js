export const DisabledMode = {
    onSetup () {
      return {} // Return empty state
    },
    onClick () {
      // Prevent feature selection
      return false
    },
    onKeyUp () {
      return false
    },
    onDrag () {
      return false
    },
    toDisplayFeatures (state, geojson, display) {
      geojson.properties.active = 'false'
      display(geojson)
    }
  }